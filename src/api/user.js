import request from '@/utils/request'

export function login(data) {
  return request({
    url: '/user/login',
    method: 'post',
    data
  })
}

export function mlogin(data) {
  return request({
    url: '/api/auth',
    method: 'post',
    data
  })
}

export function mGetInfo() {
  return request({
    url: '/api/Auth/user',
    method: 'get'
  })
}

export function getInfo(token) {
  return request({
    url: '/user/info',
    method: 'get',
    params: { token }
  })
}

export function logout() {
  return request({
    url: '/user/logout',
    method: 'post'
  })
}
